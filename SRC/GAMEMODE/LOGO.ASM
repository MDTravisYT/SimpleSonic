LOGO:
		move.l	#VRAMWRITE,	(VDPCTRL)
		LEA		LOGOART,	A0
		LEA		VDPDATA,	A1
		MOVE.W	#$61F,		D0
	@LOADGFX:
		MOVE.W	(A0)+,	(A1)
		DBF		D0,	@LOADGFX
		
	COPYTILEMAP		LOGOMAP,	$C61C,	11,	3
	
		MOVEQ	#0,	D0
	;	BSR.W	PALLOAD
		MOVE.W	#40,	(PALCYCCURRENT).W
		MOVE.W	#0,	$FFF662
		MOVE.W	#0,	$FFF660
		MOVE.W	#180,	TIMER
		MOVE.W	(VDPBUFFER).W,	D0
		ORI.B	#%1000000,	D0
		MOVE.W	D0,	(VDPCTRL).L
		
	@LOOP:
		MOVE.B	#2,	(VBLANKCMD).W
		JSR		VSYNC
	;	BSR.W	_LOGO_PALCYC
		TST.W	TIMER
		BEQ.S	@EXIT
		ANDI.B	#%10000000,	JOYPADPRESS
		BEQ.S	@LOOP
	@EXIT:
		MOVE.B	#1*4,	GAMEMODE
		RTS
		
LOGOART:	INCBIN		"ART/LOGO.UNC"
LOGOMAP:	INCBIN		"MAP/LOGO.MAP"