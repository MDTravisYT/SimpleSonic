TITLE:
		JSR		CLEARSCREEN
		move.l	#VRAMWRITE,	(VDPCTRL)
		LEA		FONT,		A0
		LEA		VDPDATA,	A1
		MOVE.W	#$17FF,		D0
	@LOADGFX:
		MOVE.W	(A0)+,	(A1)
		DBF		D0,	@LOADGFX
		
	COPYTILEMAP		TITLEMAP,	$C104,	35,	23
			
	@LOOP:
		MOVE.B	#2,	(VBLANKCMD).W
		JSR		VSYNC
		ANDI.B	#%10000000,	(JOYPADPRESS).W
		BEQ.S	@LOOP
		MOVE.B	#2*4,	(GAMEMODE).W
		RTS
		
FONT:		INCBIN		"ART/FONT.UNC"
TITLEMAP:	INCBIN		"MAP/TITLE.MAP"
TITLEPAL:	INCBIN		"PAL/TITLE.PAL"