BUBBLE:
		JSR		CLEARSCREEN
		move.l	#VRAMWRITE,	(VDPCTRL)
		LEA		FONT,		A0
		LEA		VDPDATA,	A1
		MOVE.W	#$17FF,		D0
	@LOADGFX:
		MOVE.W	(A0)+,	(A1)
		DBF		D0,	@LOADGFX
	
		LEA		TITLEPAL,	A0
		LEA		PALFADEBUFFER,	A1
		MOVE.B	#16,	D0
	@PALLOOP:
		MOVE.L	(A0)+,	(A1)+
		DBF		D0,	@PALLOOP
		
		JSR		PALFADEIN
		
		LEA		($FFFF0000),	A3
		LEA		($FFFF0001),	A4
		
	@LOOP:
		MOVE.B	#6,	(VBLANKCMD).W
		JSR		VSYNC
		MOVEQ	#0,	D3
		MOVEQ	#0,	D4

		MOVE.B	(A3),	D3
		MOVE.B	(A4),	D4
		CMP.B	D3, D4
		BHS.S	@CHK
		MOVE.B	D3,	(A4)+
		MOVE.B	D4,	(A3)+
	
		CMP.L	#$FFFF000A, A3
		BNE.S	@CONT
	@CHK:
		LEA		($FFFF0000),	A3
		LEA		($FFFF0001),	A4
		
	@CONT:
		BRA.W	@LOOP