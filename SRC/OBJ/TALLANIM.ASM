TALLANIM.FRAMETIMER	=	$1E

TALLANIM:
		MOVEQ	#0,	D0
		MOVE.B	OBJ.ACTION(A0),		D0
		MOVE.W	@INDEX(PC,D0.W),	D1
		JMP		@INDEX(PC,D1.W)
		
	@INDEX:
		DC.W	@INIT-@INDEX
		DC.W	@MAIN-@INDEX
		DC.W	@EXIT-@INDEX
		
	@INIT:
		ADDQ.B	#2,	OBJ.ACTION(A0)
		MOVE.W	#$10,	OBJ.X(A0)
		MOVE.W	#$40,	OBJ.Y(A0)
		MOVE.L	#MAP_OBJTEST,	OBJ.MAP(A0)
		MOVE.W	#$0,	OBJ.TILE(A0)
		MOVE.B	#4,	OBJ.RENDERFLAGS(A0)
		MOVE.B	#1,	OBJ.COLPROP(A0)
		MOVE.B	#3,	OBJ.FRAME(A0)
		MOVE.B	#5,	OBJ.PRIORITY(A0)
		
	@MAIN:
		BSR.W	_OBJECTDISPLAY
		SUBQ.B	#1,	TALLANIM.FRAMETIMER(A0)
		BPL.S	@EXIT
		MOVE.B	#$10,	TALLANIM.FRAMETIMER(A0)
		
	@EXIT:
		RTS